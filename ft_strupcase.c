#include "libft.h"

char	*ft_strupcase(char *str)
{
	if (!str)
		return (NULL);
	while (*str)
	{
		if (*str >= 'a' && *str <= 'z')
			*str = *str - 32;
		str++;
	}
	return (str);
}
