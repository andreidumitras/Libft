#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t	l;
	size_t	i;

	i = 0;
	l = ft_strlen(dest);
	while (src[i])
	{
		dest[l + i] = src[i];
		i++;
	}
	dest[l + i] = '\0';
	return (dest);
}
